#include<iostream>
using namespace std;


int main(){
    // Arithemtic Operators
    int a = 3;
    int b = 5;
    
    cout <<"+ "<< (a + b) << endl;  // 8
    cout <<"- "<< (a - b) << endl;  // -2
    cout <<"* "<< (a * b) << endl;  // 15
    cout <<"/ "<< (a / b) << endl;  // 0
    cout <<"% "<< (a % b) << endl;  // modulo (remainder) 3
  
  return 0;
}